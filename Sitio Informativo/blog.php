											<strong>Python</strong><br><br>
											Python es uno de los lenguajes de programación más usados actualmente y su uso sigue creciendo. Posee unas características muy potentes: es de código abierto, tiene una sintaxis sencilla y es fácil de entender, por lo que ahorra tiempo y recursos.<br>
											Python es un lenguaje versátil que puede tener múltiples aplicaciones. Una de ellas, es la Inteligencia Artificial, gracias a bibliotecas como Keras o TensorFlow.<br><br>
											<strong>Java</strong><br><br>
											Java es un lenguaje de propósito general, orientado a objetos y diseñado para tener las dependencias de implementación mínimas posibles. Con Java se pueden crear aplicaciones y procesos en múltiples dispositivos, por lo que es una muy buena opción si te preguntas qué lenguaje de programación aprender en 2022.<br>
											Su ámbito de aplicación es muy amplio, por lo que permite crear software para dispositivos móviles, terminales de venta, IoT, además de páginas web.<br><br>
											<strong>C#</strong><br><br>
											C# es otro de los mejores lenguajes de programación. Fue creado por Microsoft y está presente en entornos empresariales, como instituciones gubernamentales, entidades bancarias o médicas, entre otros.<br>
											Aunque más allá de este tipo de aplicaciones, tiene también usos muy versátiles como el IoT, desarrollo de videojuegos, web o aplicaciones móviles…<br><br>
											<strong>C++</strong><br><br>
											C++ se caracteriza por ser extremadamente rápido y estable, lo que lo sitúa como uno de los mejores lenguajes de programación y, por tanto, también uno de los lenguajes de programación más usados. Además, proporciona STL (biblioteca de plantillas estándar), es decir, un conjunto de bibliotecas listas para usar para diferentes estructuras de datos, operaciones aritméticas y algoritmos.<br>
											El soporte de estas bibliotecas y la velocidad del lenguaje lo convierten en una opción muy popular entre la comunidad comercial de HFT (High Frecuency Trading).<br><br>
											<strong>JavaScript</strong><br><br>
											JavaScript es uno de los lenguajes de programación más usados para crear páginas web dinámicas. Permite a los programadores implementar funciones complejas en las páginas web para añadirles más interactividad.<br>
											Una página dinámica es aquella que incorpora efectos, como texto que aparece y desaparece, animaciones, acciones que se activan al pulsar botones y ventanas con mensajes de aviso al usuario.
